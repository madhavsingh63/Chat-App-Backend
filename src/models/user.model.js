import mongoose from "mongoose";

const userSchema = new mongoose.Schema(
  {
    email: {
      type: String,
      required: true,
      unique: true,
    },
    fullName: {
      type: String,
      required: true,
    },
    password: {
      type: String,
      required: true,
      minlength: 6,
    },
    profilePicture: {
      type: String,
      default: "https://www.gravatar.com/avatar/?d=mp&f=y",
    },
  },
  { timestamps: true }
);

export default mongoose.model("User", userSchema);
